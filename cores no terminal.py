#utilizando os códigos de escape sequence ANSI para configurar cores para os seus programas em Python.
# Veja como utilizar o código \033[m com todas as suas principais possibilidades.

print('\033[1;41;43mOlá, mundo! \033[m') #fundo amarelo

print('\033[1;41;44mOlá, mundo! \033[m') #fundo azul

print('\033[1;41;45mOlá, mundo! \033[m') #fundo rosa

print('\033[1;41;46mOlá, mundo! \033[m') #fundo ciano

print('\033[1;41;47mOlá, mundo! \033[m') #fundo cinza

print('\033[1;41;42mOlá, mundo! \033[m') #fundo verde

print('\033[1;41;40mOlá, mundo! \033[m') #fundo preto